                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Sudah menjadi anggota koperasi selama 3 bulan</li> 
                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Bersedia mengikuti pertemuanrutin kelompok</li> 
                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Pinjaman pertama maksimal Rp. 500.000 </li> 
