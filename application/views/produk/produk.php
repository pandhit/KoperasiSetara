                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Dana pinjaman reguler yang bisa diakses maksimal sebesar RP. 25.000.000 dan pinjaman khusus maksimal Rp. 50.000.000(mulai tahun 2016)</li> 
                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Pinjaman diatas Rp. 3.000.000 memakai agunan/jaminan</li> 
                                    <li style="font-family: Montserrat-Regular;font-size: 15px;color: #888888;">- Setuju dengan sistem tanggung renteng dan direkomendasi oleh anggota kelompoknya dan persetujuan keluarga</li> 
								</p>
                                <h6 class="txtProd" style="margin-bottom: 1%;">
								<strong>Jenis Simpanan meliputi : </strong>	
								</h6>
								<ul style= "font-family: Montserrat-Regular;font-size: 15px;color: #888888;margin-left:1%">
								<li >1. Simpanan Pokok (SP). Simpanan ini merupakan simpanan wajib awal ketika menjadi anggota koperasi. Simpanan ini akan diberi SHU setiap tahun</li>
								<li>2. Simpanan Wajib(SW). Simpanan ini dibayarkan oleh anggota setiap bulannya. Simpanan ini diberi SHU setiap tahun </li>
								<li>3. Simpanan Wajib Khusus(SWK). Simpanan ini dimaksudkan untuk menambah plafon pinjaman anggota, maka baru bisa diambil setelah anggota keluar dari koperasi dan akan mendapatkan SHU.</li>
								<li>4. Simpanan Hari Raya (SHR), dengan jasa 6%/tahun dan dapat diambil satu bulan menjelang hari raya</li>
								<li>5. Simpanan Pendidikan (SPD), dengan jasa 6%/tahun dan dapat diambil sesuai dengan kebutuhan dana menurut kalender pendidikan</li>
								<li>6. Simpanan Mana Suka (SMS) dengan jasa 3% per tahun dan dapat diambil sewaktu waktu</li>
								</ul>
